export function findNutritionMatches(foodName, { limit = 3 } = {}) {
  const normalized = normalizeFoodName(foodName);
  if (!normalized) return [];

  const scored = nutritionKnowledge
    .map((entry) => {
      const names = [entry.food, ...(entry.aliases || [])].map(normalizeFoodName);
      const exact = names.some((name) => name === normalized);
      const contains = names.some((name) => normalized.includes(name) || name.includes(normalized));
      const component = names.some((name) => name.length >= 2 && normalized.includes(name));
      const score = exact ? 3 : contains ? 2 : component ? 1 : 0;
      return { entry, score };
    })
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score || a.entry.food.length - b.entry.food.length);

  const bestScore = scored[0]?.score || 0;
  const matches = scored
    .filter((item) => (bestScore >= 3 ? item.score >= 3 : item.score > 0))
    .slice(0, limit)
    .map((item) => ({
      food: item.entry.food,
      group: item.entry.group,
      portion: item.entry.portion,
      carbs: item.entry.carbs,
      gi: item.entry.gi,
      gl: item.entry.gl,
      insulinIndex: item.entry.insulinIndex,
      glLevel: classifyGl(item.entry.gl),
      iiLevel: classifyIi(item.entry.insulinIndex),
      note: item.entry.note,
      matchScore: item.score,
    }));

  return matches;
}

function normalizeFoodName(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[（）()·\s]/g, "")
    .replace(/清炒|凉拌|水煮|蒸|炒|烤|煎|炸|红烧/g, "")
    .trim();
}

function classifyGl(value) {
  if (!Number.isFinite(value)) return "unknown";
  if (value <= 10) return "low";
  if (value <= 19) return "medium";
  return "high";
}

function classifyIi(value) {
  if (!Number.isFinite(value)) return "unknown";
  if (value < 50) return "low";
  if (value < 80) return "medium";
  return "high";
}
